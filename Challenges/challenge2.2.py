"""
Input                              Output:
─────────────────────────────────  ────────────────────────────
([192,168,17,2], [255,0,0,0])      ([192,0,0,0], [0,168,17,2])
([192,168,17,2], [255,255,0,0])    ([192,168,0,0], [0,0,17,2])
([192,168,17,2], [255,255,255,0])  ([192,168,17,0], [0,0,0,2])
([192,168,17,2], [255,192,0,0])    ([192,128,0,0], [0,40,17,2])
([192,168,17,2], [255,255,248,0])  ([192,168,16,0], [0,0,1,2])
"""

inputs = [([192,168,17,2], [255,0,0,0]), ([192,168,17,2], [255,255,0,0]), ([192,168,17,2], [255,255,255,0]), 
          ([192,168,17,2], [255,192,0,0]), ([192,168,17,2], [255,255,248,0])]

def getNetworkAndHost(ipSubnets):
    ip = ipSubnets[0]
    subnet = ipSubnets[1]
    network = []
    host = []
    for i in range(len(ip)):
        network.append(ip[i] & subnet[i])
        inverse = ~subnet[i] & 0xff
        host.append(ip[i] & inverse)
    return (network, host)

for ipAndMask in inputs:
    print(getNetworkAndHost(ipAndMask))
